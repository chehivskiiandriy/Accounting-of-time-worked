import swal from 'sweetalert2';

export function success() {
    swal({
        title: 'Great!',
        text: 'Your work has been saved!',
        type: 'success',
        width: '300px',
        showConfirmButton: false,
        timer: 1500
    });
}

export function error() {
    swal({
        title: 'Oops...',
        text: 'Something went wrong!',
        type: 'error',
        width: '300px',
        showConfirmButton: false,
    });
} 